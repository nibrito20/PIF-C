#include <stdio.h>

int main(){

    int i = 12; // Variável inteira de nome "i" de valor 0. Caso não der um valor à variável, o programa atribui a ela um valor aleatório.
    printf("%d", i); // Para imprimir números inteiro usamos "%d".

    /*
        1) char - 1 caracter - %c;
        2) int - números inteiros - %d;
        3) float - números reais - %f;
        4) double - números reais - %lf (long float);
        5) string - lista de nomes - %s;
        6) array - lista de números inteiros ou reias - %d, %f, %lf;
    */


    return 0;
    
}


